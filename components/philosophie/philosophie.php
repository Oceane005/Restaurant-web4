  <span class="latin-title">Philosophie culinaire</span>
  <p>
    La cuisine japonaise est une question de respect : respect du produit, du geste et des
    convives. Il prône une approche minimaliste où chaque élément est parfaitement exécuté
    pour révéler le meilleur de l'ingrédient. La simplicité et la précision sont au cœur de
    ses créations.
